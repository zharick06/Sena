package Procesos;

public class Usuarios {
	private String Nombre;
	private int Edad;

	public int getEdad() {
		return Edad;
	}

	public void setEdad(int edad) {
		Edad = edad;
	}

	public String getNombre() {
		return Nombre;
	}

	public void setNombre(String nombre) {
		Nombre = nombre;
	}

	@Override
	public String toString() {
		return "Nombre: " + Nombre + ", Edad: " + Edad ;
	}
	

}
